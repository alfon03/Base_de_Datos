CREATE OR REPLACE PROCEDURE SERIE (MINIMO NUMBER, MAXIMO NUMBER, PASO NUMBER)
IS
  CONTADOR NUMBER := MINIMO;
BEGIN
  WHILE CONTADOR <= MAXIMO LOOP
    DBMS_OUTPUT.PUT_LINE(CONTADOR);
    CONTADOR := CONTADOR + PASO;
  END LOOP;
END;

----
BEGIN
  SERIE(1, 10, 2);
END;

