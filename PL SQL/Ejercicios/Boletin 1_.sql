CREATE OR REPLACE FUNCTION NOTA (NOTA NUMBER)
RETURN varchar2
IS
BEGIN 
	IF (NOTA>10 OR NOTA<0) THEN
		RETURN ('Valores no válidos');
	ELSIF (NOTA < 5) THEN
		RETURN ('Insuficiente');
	ELSIF (NOTA = 5) THEN
		RETURN ('Suficiente');
	ELSIF (NOTA > 5 AND NOTA <=7) THEN
		RETURN ('Bien');
	ELSIF (NOTA > 7 AND NOTA <=8) THEN
		RETURN ('Notable');
	ELSIF (NOTA > 8 AND NOTA <=10) THEN
		RETURN ('Sobresaliente');
	END IF;
END;
