CREATE OR REPLACE FUNCTION AZAR(MINIMO NUMBER, MAXIMO NUMBER) RETURN NUMBER
IS
  NUM_AZAR NUMBER;
BEGIN
  NUM_AZAR := ROUND(DBMS_RANDOM.VALUE(MINIMO, MAXIMO));
  RETURN NUM_AZAR;
END;
----
